    private String getVendorIdFromName(String vendorName) {
        try (BufferedReader br = new BufferedReader(new FileReader("users.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(";");
                if (data.length > 1 && vendorName.equals(data[1])) { // Vendor name is at index 1
                    return data[0]; // Assuming the first field is the vendor ID
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null; // Vendor not found
    }

    private void loadDishes() {
        allDishes.clear();
        try (BufferedReader br = new BufferedReader(new FileReader("dishes.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] dishData = line.split(",");
                if (dishData.length == 7) { // Check if we have all expected fields
                    allDishes.add(List.of(dishData)); // Store the whole line including userId
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void updateTableWithItems(String vendorId) {
        if (vendorId == null) {
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (List<String> dish : allDishes) {
            if (dish.get(6).equals(vendorId)) { // Check if the dish belongs to the selected vendor, userId is now last
                model.addRow(new Object[]{dish.get(0), dish.get(1), dish.get(2), dish.get(3), dish.get(4), dish.get(5)});
            }
        }
    }
    
    private void populateVendorComboBox() {

        List<String> vendors = new ArrayList<>();
        try (BufferedReader br = new BufferedReader(new FileReader("users.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(";");
                if (data.length > 3 && "Vendor".equals(data[3])) {
                    vendors.add(data[1]); // Vendor name is at index 1
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        // Clear existing items in the combo box and add new ones
        jComboBox1.removeAllItems();
        for (String vendor : vendors) {
            jComboBox1.addItem(vendor);
        }
    }
    
