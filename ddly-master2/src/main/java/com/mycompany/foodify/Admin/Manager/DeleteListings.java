    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        String selectedVendor = (String) jComboBox1.getSelectedItem();
        String vendorId = getVendorIdFromName(selectedVendor);
        if (vendorId != null) {
            updateTableWithItems(vendorId);
        } else {
            JOptionPane.showMessageDialog(this, "Vendor not found.");
        }
    }//GEN-LAST:event_confirmBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String dishId = (String) jTable1.getValueAt(selectedRow, 0);

            // Find and remove the dish from allDishes
            allDishes.removeIf(d -> d.get(0).equals(dishId));

            // Remove from table model
            ((DefaultTableModel) jTable1.getModel()).removeRow(selectedRow);

            // Update the file
            try {
                List<String> updatedLines = new ArrayList<>();
                try (BufferedReader br = new BufferedReader(new FileReader("dishes.txt"))) {
                    String line;
                    while ((line = br.readLine()) != null) {
                        String[] dishData = line.split(",");
                        if (dishData.length == 7 && !dishData[0].equals(dishId)) {
                            updatedLines.add(line);
                        }
                    }
                }

                // Write updated content back to file
                try (FileWriter writer = new FileWriter("dishes.txt")) {
                    for (String updatedLine : updatedLines) {
                        writer.write(updatedLine + "\n");
                    }
                }
                JOptionPane.showMessageDialog(this, "Dish removed successfully.");
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error removing dish: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a dish to remove.");
        }
                                     
    }//GEN-LAST:event_removeBtnActionPerformed

